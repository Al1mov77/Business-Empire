export function t(key: keyof typeof TRANSLATIONS.en, language: 'en' | 'ru', variables?: Record<string, string | number>): string {
  const dict = TRANSLATIONS[language || 'en'] || TRANSLATIONS.en;
  let text = dict[key] || TRANSLATIONS.en[key] || String(key);

  if (variables) {
    Object.entries(variables).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, String(v));
    });
  }
  return text;
}


